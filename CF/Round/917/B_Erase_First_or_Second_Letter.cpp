#include <vector>
using namespace std;

#define endl '\n'
using LL=long long;

void solve() {
    int n;
    string s;
    cin>>n>>s;
    vector<int> cnt(1<<7);
    LL ans=0,dif=0;
    for(char x:s) {
        if(++cnt[x]==1) dif++;
        ans+=dif;
    }
    cout<<ans<<endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--) solve();
    return 0;
}